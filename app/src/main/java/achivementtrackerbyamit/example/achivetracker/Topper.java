package achivementtrackerbyamit.example.achivetracker;

public class Topper {
    String Goal_Name;
    String Consistency;

    public Topper(String goal_Name, String consistency) {
        Goal_Name = goal_Name;
        Consistency = consistency;
    }

    public String getGoal_Name() {
        return Goal_Name;
    }

    public String getConsistency() {
        return Consistency;
    }
}
